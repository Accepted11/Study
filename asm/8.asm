ZERO EQU 3FH
ONE EQU 06H
TWO EQU 5BH
THREE EQU 4FH
FOUR EQU 66H
FIVE EQU 6DH
SIX EQU 7DH
SEVEN EQU 07H
EIGHT EQU 7FH
NINE EQU 6FH

STACK SEGMENT STACK
DW 32 DUP(?)
SSTACK ENDS
DATAS SEGMENT
LED DB ZERO,ONE,TWO,THREE,FOUR,FIVE,SIX,SEVEN,EIGHT,NINE
DATAS ENDS
CODES SEGMENT

ASSUME CS:CODES,DS:DATAS
START:
MOV AX,DATAS
MOV DS,AX
MOV DX,0606H
MOV AL,80H
OUT DX,AL

AA3:
MOV CX,10
MOV BL,11011111B
MOV SI,0


AA1:
MOV DX,0602H
MOV AL,LED[SI]
OUT DX,AL
MOV DX,0600H
MOV AL,BL
OUT DX,AL
CALL DELAY
CALL DELAY
CALL DELAY

MOV DX,0600H
MOV AL,0FFH
OUT DX,AL

INC SI
LOOP AA1

JMP AA3


DELAY:
PUSH CX
MOV CX,0FFFFH
AA2:
PUSH AX
POP AX
LOOP AA2
POP CX
RET

CODE ENDS
END START

