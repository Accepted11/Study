function [c,ceq,k1,k2,s]=fun8(x,s);
%s为向量w的采样值
c=[];ceq=[];
%初始化样本间距，s有ntheta行2列
if isnan(s(1,1)) %tf=isnan(A)：返回一个与A相同维数的数组，
                 %若A的元素为NaN（非数值），在对应位置上返回逻辑1（真），否则返回逻辑0（假）。
    s=[0.2,0;0.2 0];
end
%计算样本集
w1=1:s(1,1):100;
w2=1:s(2,1):100;
%半无穷约束
k1=sin(w1*x(1)).*cos(w1*x(2))-1/1000*(w1-50).^2-sin(w1*x(3))-x(3)-1;
k2=sin(w2*x(2)).*cos(w2*x(1))-1/1000*(w2-50).^2-sin(w2*x(3))-x(3)-1;
%画出半无穷约束的图形
plot(w1,k1,'-',w2,k2,'+');
