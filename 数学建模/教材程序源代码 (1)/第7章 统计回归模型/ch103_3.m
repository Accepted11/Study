x=[0.02 0.02 0.06 0.06 0.11 0.11 0.22 0.22 0.56 0.56 1.10 1.10];
y=[76 47 97 107 123 139 159 152 191 201 207 200];
z=1./y;
u=1./x;
a=polyfit(u,z,1);
zz=polyval(a,u);
subplot(1,2,1)
plot(u,z,'o',u,zz)
axis([0 60 0 0.025])

b(1)=1/a(2)
b(2)=a(1)/a(2)
xx=0:0.01:1.2;
yy=b(1)*xx./(b(2)+xx);
subplot(1,2,2)
plot(x,y,'o',xx,yy)
axis([0 1.2 0 250])